# в переменную запишется результат сравнения, 
# то есть true или false
a = 1 > 4 
print(a)

# Так же с результатми сравнений 
# можно производить логические дейтсвия
# В данном случае это будет логическое 'и'
a = 1 < 4 and 5 > 2
print(a)

# Проверка полного равенства одного числа другому
a = 1 == 2
print(a)

# Проверка неравенства чисел
a = 1 != 2
print(a)

# Проверка равенства\идентичности строк
a = 'qwe'
b = 'qwe'
print(a == b)

# сравнение нескольких чисел
a = 1 < 3 < 5 < 10
print (a)